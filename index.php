  <div class="cours">
    <h2>Les cours</h2><br>
    <p>Notre formation est divisée en trois parties</p>
    <ul>
      <li><p>Travaux théoriques</p></li>
      <li><p>Travaux dirigées</p></li>
      <li><p>Travaux personnels</p></li>
    </ul>
    <p>Les cours théoriques sont composés de tout document (slides, pdf, livres etc ...) ou vidéo illustrant la partie théorique des TD que vous ferez en présentiel (2 heures par jours pour chaque matière), afin que vous puissiez d'abord comprendre la théorie avant de passer à la pratique, voire même anticiper certains cours.Pour chaque cours théorique diffusé, les TD relatifs à ceux-ci seront effectués la semaine d'après la diffusion.</p>
  </div>
  <div class="parcours">
    <h2>les parcours</h2>
    <p>HEI propose deux parcours : Ecosystèmes Logiciels (EL) et Transformations Numériques (TN).</p>
    <p>Le parcours EL produit des profils capables de programmer les briques de systèmes informatiques
complexe. Il mène aux débouchés suivants : développeur backend, développeur frontend,
développeur mobile, analyste de données, chef de projet, consultant en innovation.
Le parcours TN produit des profils capable d’exploiter les briques déjà programmées de systèmes
informatiques complexes. Il mène aux débouchés suivants : référenceur web, gestionnaire web,
commerçant-marketeur digital, analyste de données, devOps, chef de projet, consultant en
innovation.</p>
  </div>
